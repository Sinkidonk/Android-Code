public class Ball {
    private float mX;
    private float mY;
    private float mTop;
    private float mBottom;
    private float mLeft;
    private float mRight;
    private float mVelocityX;
    private float mVelocityY;
    private int mWidth;

    public float getX() {
        return mX;
    }

    public void setX(float mX) {
        this.mX = mX;
    }

    public float getY() {
        return mY;
    }

    public void setY(float mY) {
        this.mY = mY;
    }

    public float getTop() {
        return mTop;
    }

    public void setTop(float mTop) {
        this.mTop = mTop;
    }

    public float getBottom() {
        return mBottom;
    }

    public void setBottom(float mBottom) {
        this.mBottom = mBottom;
    }

    public float getLeft() {
        return mLeft;
    }

    public void setLeft(float mLeft) {
        this.mLeft = mLeft;
    }

    public float getRight() {
        return mRight;
    }

    public void setRight(float mRight) {
        this.mRight = mRight;
    }

    public float getVelocityX() {
        return mVelocityX;
    }

    public void setVelocityX(float mVelocityX) {
        this.mVelocityX = mVelocityX;
    }

    public float getVelocityY() {
        return mVelocityY;
    }

    public void setVelocityY(float mVelocityY) {
        this.mVelocityY = mVelocityY;
    }


    public int getWidth() {
        return mWidth;
    }

    public void setWidth(int mWidth) {
        this.mWidth = mWidth;
    }
}